package com.github.wmarkow.distiller.domain.calc;

public class CondensationSpeed {
    public final double speedInKgPerSec;
    public final double speedInLPerSec;

    public CondensationSpeed(double speedInKgPerSec, double speedInLPerSec) {
        this.speedInKgPerSec = speedInKgPerSec;
        this.speedInLPerSec = speedInLPerSec;
    }
}
